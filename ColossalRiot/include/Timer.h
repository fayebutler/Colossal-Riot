#ifndef TIMER_H
#define TIMER_H

#include <SDL.h>

//----------------------------------------------------------------------------------------------------------------------
/// @file Timer.h
/// @class Timer "include/Timer.h"
/// @brief A monostate class that contains all the time information to be used throughout the game
/// @author Will Herbert
/// @version 1.0
/// @date Last revision 10/05/2015 Updated to comply with doxygen and NCCA coding standard
//----------------------------------------------------------------------------------------------------------------------

class Timer
{
public:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief ctor this will create a timer with a start time of SDL_GetTicks()
  //----------------------------------------------------------------------------------------------------------------------
  Timer();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief gets the current time of the game
  /// @return double returns the current time of the game
  //----------------------------------------------------------------------------------------------------------------------
  double getCurrentTime();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief resets the start time of the game to SDL_GetTicks()
  //----------------------------------------------------------------------------------------------------------------------
  void resetTimer();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief  calculates the time elapsed since last called
  //----------------------------------------------------------------------------------------------------------------------
  void calculateTimeElapsed();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief gets the time elapsed since last tick
  /// @return returns the time elapsed since last tick
  //----------------------------------------------------------------------------------------------------------------------
  double getTimeElapsed() { return s_timeElapsed; }

private:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief the current time of the game
  //----------------------------------------------------------------------------------------------------------------------
  static long long s_currentTime;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief the time at which the game started
  //----------------------------------------------------------------------------------------------------------------------
  static long long s_startTime;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief the time elapsed between each tick
  //----------------------------------------------------------------------------------------------------------------------
  static double s_timeElapsed;
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief the time of the previous tick
  //----------------------------------------------------------------------------------------------------------------------
  static double s_previousTickTime;

};


#endif // TIMER_H
